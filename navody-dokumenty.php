                    <p>Pokud jste si zařídili webhosting předem a nevytváříme nový spolu, potřebujeme dočasný přístup k vašemu hostingu, abychom mohli web správně nastavit a spustit. Postupujte podle tohoto jednoduchého návodu, aby vše proběhlo bezpečně a rychle:</p>
                    
