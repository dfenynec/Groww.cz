                        <ol class="fs-17 text-dark-gray mb-0">
                            <li class="mb-2"><strong>Přidejte nás jako uživatele</strong> – Přihlaste se do administrace vašeho webhostingu (např. Český hosting, Webglobe, Forpsi apod.) a v sekci „Uživatelé“ nebo „Přístupy“ přidejte náš e-mail <code>info@growwdigital.co</code> s oprávněním správce nebo technika.</li>
                            <li class="mb-2"><strong>Neposílejte hesla e-mailem</strong> – Pokud není možné přidat uživatele, vytvořte dočasné silné heslo, které po skončení spolupráce ihned změníte. Nikdy neposílejte hesla nechráněným způsobem (e-mail, chat apod.).</li>
                            <li><strong>Potvrďte nám přístup</strong> – Po přidání uživatele nebo vytvoření dočasného hesla nám napište, že je přístup připravený. Po dokončení projektu můžete náš přístup kdykoliv odebrat.</li>
